#operadores aritmeticos
n1 = int(input("ingresa numero uno "))
n2 = int(input("ingresa numero dos "))
print("la suma es " + str(n1+n2))
print("la resta es " + str(n1-n2))
print("la multiplicación es " + str(n1*n2))
print("la división es " + str(n1/n2))
print("el cociente de la división es " + str(n1//n2))
print("el modulo es es " + str(n1%n2))

